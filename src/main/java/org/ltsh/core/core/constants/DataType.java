package org.ltsh.core.core.constants;

/**
 * 通用数据类型枚举类
 * @author Ych
 * 2018年8月3日
 */
public enum DataType {

	/**
	 * 空
	 */
	BLANK,
	/**
	 * 字符串
	 */
	STRING,
	/**
	 * 数字（包括所有数字）
	 */
	NUMERIC,
	/**
	 * 单精度浮点
	 */
	FLOAT,
	/**
	 * 双精度浮点
	 */
	DOUBLE,
	/**
	 * 布尔值
	 */
	BOOLEAN,
	/**
	 * 字节
	 */
	BYTE,
	/**
	 * 整形
	 */
	INTEGER,
	/**
	 * 整形
	 */
	LONG,
	/**
	 * 日期
	 */
	DATE,
	/**
	 * 时间
	 */
	TIME,
	/**
	 * 日期+时间
	 */
	DATETIME,
	/**
	 * 时间戳
	 */
	TIMESTAMP,
	
	/**
	 * 金额
	 */
	MONEY,
	/**
	 * 文件
	 */
	FILE,
	/**
	 * 视频
	 */
	VIDEO,
	
	/**
	 * 音频
	 */
	RADIO
}
